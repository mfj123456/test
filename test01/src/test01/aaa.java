package test01;

public class aaa {

	public String name;
	public Integer age;
	public Integer a;

	public aaa(String name, Integer age, Integer a) {
		super();
		this.name = name;
		this.age = age;
		this.a = a;
	}

	public aaa() {
		super();
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}

	public Integer getA() {
		return a;
	}

	public void setA(Integer a) {
		this.a = a;
	}

}
